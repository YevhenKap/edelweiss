/**
 * @throws {TypeError} if *datetime* isn't provided and its children isn't a valid date.
 */
export function time(
  children?: ElementChildren,
  attributes?: Attributes,
  listeners?: ElementEventListenersObject
) {
  if (attributes && !attributes.datetime) {
    if (children && typeof children !== 'string') {
      throw new TypeError(
        'If the <time> node does not have a datetime attribute, it must not have any element descendants, but a time value as a string.'
      )
    }
  }
  return element('time', children, attributes, listeners)
}

/**
 * @throws {TypeError} children is not an string.
 */
export function title(
  children?: ElementChildren,
  attributes?: Attributes,
  listeners?: ElementEventListenersObject
) {
  if (children && typeof children !== 'string') {
    throw new TypeError(
      `Children of the <title> node must be string, but got ${typeof children}`
    )
  }
  return element('title', children, attributes, listeners)
}

